#!/bin/bash
# Установка и запуск Playwright тестов в виртуальном окружении

echo "============================================================"
echo "🎭 НАСТРОЙКА PLAYWRIGHT ДЛЯ E2E ТЕСТИРОВАНИЯ"
echo "============================================================"

# Создаем виртуальное окружение
echo "📦 Создание виртуального окружения..."
python3 -m venv test_env

# Активируем виртуальное окружение
echo "🔧 Активация виртуального окружения..."
source test_env/bin/activate

# Устанавливаем playwright
echo "📥 Установка Playwright..."
pip install playwright

# Устанавливаем браузеры
echo "🌐 Установка Chromium..."
playwright install chromium

# Запускаем тест
echo ""
echo "🚀 Запуск E2E тестов..."
python3 test_all_pages.py

# Деактивируем виртуальное окружение
deactivate

echo ""
echo "✅ Тестирование завершено!"
echo "============================================================"